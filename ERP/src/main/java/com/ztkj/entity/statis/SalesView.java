package com.ztkj.entity.statis;

public class SalesView {
	
	private String f_name;
	private String f_did;
	private double f_count;
	private double f_money;
	private String f_time;
	private String f_auditor;
	public String getF_name() {
		return f_name;
	}
	public void setF_name(String f_name) {
		this.f_name = f_name;
	}
	public String getF_did() {
		return f_did;
	}
	public void setF_did(String f_did) {
		this.f_did = f_did;
	}
	public double getF_count() {
		return f_count;
	}
	public void setF_count(double f_count) {
		this.f_count = f_count;
	}
	public double getF_money() {
		return f_money;
	}
	public void setF_money(double f_money) {
		this.f_money = f_money;
	}
	public String getF_time() {
		return f_time;
	}
	public void setF_time(String f_time) {
		this.f_time = f_time;
	}
	public String getF_auditor() {
		return f_auditor;
	}
	public void setF_auditor(String f_auditor) {
		this.f_auditor = f_auditor;
	}
	@Override
	public String toString() {
		return "SalesView [f_name=" + f_name + ", f_did=" + f_did + ", f_count=" + f_count + ", f_money=" + f_money
				+ ", f_time=" + f_time + ", f_auditor=" + f_auditor + "]";
	}
	
}
